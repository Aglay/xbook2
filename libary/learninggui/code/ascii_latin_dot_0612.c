static  int  is_in_ascii_d0612_block(const TCHAR *code)
{
    if ( *code < 0x20 )
        return  -1;

    if ( *code > 0x7E )
        return  -1;

    return  1;
}

static  unsigned int  get_ascii_d0612_start_index(const TCHAR *code)
{
    unsigned int  index = (*code)*9 - 288;

    if ( index > 846 )
        index = 846;

    return  index;
}

